<h3>請使用for迴圈來產生1,3,5,7,9……n數列</h3>
<?php
$n=100;
$count=0;
for($i=1;$i<$n;$i=$i+2){
    $count++;
    echo $i . ",";
}
echo "<br>";
echo "迴圈數:".$count;
?>
